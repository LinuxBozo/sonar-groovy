package org.sonar.plugins.groovy.codenarc;

import org.sonar.api.profiles.ProfileExporter;
import org.sonar.api.profiles.RulesProfile;
import org.sonar.api.rules.ActiveRule;
import org.sonar.api.rules.ActiveRuleParam;
import org.sonar.api.utils.SonarException;
import org.sonar.plugins.groovy.foundation.Groovy;

import java.io.IOException;
import java.io.Writer;
import java.util.List;

public class CodeNarcProfileExporter extends ProfileExporter {

  public CodeNarcProfileExporter() {
    super(CodeNarcRuleRepository.REPOSITORY_KEY, CodeNarcRuleRepository.REPOSITORY_NAME);
    setSupportedLanguages(Groovy.KEY);
    setMimeType("application/xml");
  }

  @Override
  public void exportProfile(RulesProfile profile, Writer writer) {
    try {
      generateXML(writer, profile.getActiveRulesByRepository(CodeNarcRuleRepository.REPOSITORY_KEY));

    } catch (IOException e) {
      throw new SonarException("Fail to export CodeNarc profile : " + profile, e);
    }
  }

  private void generateXML(Writer writer, List<ActiveRule> activeRules) throws IOException {
    appendXmlHeader(writer);
    for (ActiveRule activeRule : activeRules) {
      appendRule(writer, activeRule);
    }
    appendXmlFooter(writer);
  }

  private void appendXmlHeader(Writer writer) throws IOException {
    writer.append("<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n")
        .append("<!-- Generated by Sonar -->\n")
        .append("<ruleset xmlns=\"http://codenarc.org/ruleset/1.0\"\n")
        .append("         xmlns:xsi=\"http://www.w3.org/2001/XMLSchema-instance\"\n")
        .append("         xsi:schemaLocation=\"http://codenarc.org/ruleset/1.0 http://codenarc.org/ruleset-schema.xsd\"\n")
        .append("         xsi:noNamespaceSchemaLocation=\"http://codenarc.org/ruleset-schema.xsd\">\n");
  }

  private void appendXmlFooter(Writer writer) throws IOException {
    writer.append("</ruleset>");
  }

  private void appendRule(Writer writer, ActiveRule activeRule) throws IOException {
    writer.append("<rule class=\"")
        .append(getRuleClass(activeRule))
        .append("\">\n");
    appendProperty(writer, "name", activeRule.getRuleKey());
    appendProperty(writer, "description", getRuleDescription(activeRule));
    for (ActiveRuleParam activeRuleParam : activeRule.getActiveRuleParams()) {
        appendProperty(writer, activeRuleParam.getKey(), activeRuleParam.getValue());
    }
    writer.append("</rule>\n");
  }
  
  private void appendProperty(Writer writer, String name, String value) throws IOException {
      writer.append("<property name=\"")
          .append(name)
          .append("\" value=\"")
          .append(value)
          .append("\"/>\n");  
  }
  
  private String getRuleClass(ActiveRule activeRule) {
      String ruleKey = activeRule.getRuleKey();
      return ruleKey.replaceAll("_\\d+$", "");
  }

  private String getRuleDescription(ActiveRule activeRule) {
      String description = activeRule.getRule().getDescription();
      if (description != null) {
        description = description.replaceAll("\\<.*?\\>", "").replaceAll("\"", "'");
      } else {
        description = "";
      }
      return description;
  }
}
